  /**
   * Create/save a new user.
   * POST users
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async store ({ request, response, auth }) {
    const data = request.only(['name', 'email', 'password'])

    // Traz todos os id teams [1, 2, 3] e armazena em teams
    const teamsQuery = Invite.query().where('email', data.email)
    const teams = await teamsQuery.pluck('team_id')
    console.log(teams)
    if (teams.length === 0) {
      return response.status(401).send({ message: 'Você não está convidado para nenhum time.' })
    }
    const user = await User.create(data)

    await user.teams().attach(teams)

    await teamsQuery.delete()

    const token = await auth.attempt(data.email, data.password)

    return token
  }

